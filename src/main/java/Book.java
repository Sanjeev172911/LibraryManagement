public class Book {
    String title;
    String author;
    String publisher;
    String genre;
    int numberOfPages;

    Book(String title,String author,String publisher,String  genre,int numberOfPages){
        this.title=title;
        this.author=author;
        this.publisher=publisher;
        this.genre=genre;
        this.numberOfPages=numberOfPages;
    }


}
